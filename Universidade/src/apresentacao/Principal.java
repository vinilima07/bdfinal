    private void atualizaDadosDepartamento(){
        DepartamentoDAO deptoDAO = new DepartamentoDAO();
        Departamento depto = deptoDAO.consultaDepartamento(jComboBox6.getSelectedItem().toString());
        Object[][] dados = new Object[depto.getProfessor().size()][4];
        Iterator<Professor> it = depto.getProfessor().iterator();
        int i = 0;
        while (it.hasNext()){
            Professor aux = it.next();
            dados[i][0] = depto.getCodigoDepto();
            dados[i][1] = depto.getSiglaDepto();
            dados[i][2] = depto.getNomeDepto();
            dados[i][3] = aux.getNomePessoa();
            i++;
        }
        jTable2.setModel(new javax.swing.table.DefaultTableModel(
        dados,
        new String [] {
            "CÃ³digo", "Sigla", "Nome", "Professores"
        }));  
    }
